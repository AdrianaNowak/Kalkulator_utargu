    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
     utarg1 = Double.parseDouble(jKasjer1.getText());
     utarg2 = Double.parseDouble(jKasjer2.getText());
     utarg3 = Double.parseDouble(jKasjer3.getText());
     utargDnia = obliczUtarg(utarg1,utarg2,utarg3);
    jUtargDnia.setText(utargDnia);   
    
     double[] utargi = {utarg1,utarg2,utarg3};
     maxUtarg = obliczNajwyzszyUtarg(utargi);
     
     jMaxUtarg.setText(maxUtarg);
    }//GEN-LAST:event_jButton1ActionPerformed

    private String obliczNajwyzszyUtarg(double[] tablicaUtargow){
        double localMaxUtarg=0;
        for (int i = 0; i < tablicaUtargow.length; i++) {            
            if(tablicaUtargow[i] > localMaxUtarg){                
                localMaxUtarg = tablicaUtargow[i];
            } 
        }
        return String.valueOf(localMaxUtarg);
    }           
            
    private String obliczUtarg(double u1, double u2, double u3){        
        double suma = u1+u2+u3;
        String sumaTekst = String.valueOf(suma);  // parsowanie do stringa
        return sumaTekst;
    }
    
